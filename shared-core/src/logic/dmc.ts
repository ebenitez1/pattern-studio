// Lab conversion + nearest match
// ---------------------------------------------------------------------------

function srgbToLinear(v: number): number {
  const c = v / 255;
  return c <= 0.04045 ? c / 12.92 : Math.pow((c + 0.055) / 1.055, 2.4);
}

function rgbToLab(r: number, g: number, b: number): [number, number, number] {
  const rl = srgbToLinear(r);
  const gl = srgbToLinear(g);
  const bl = srgbToLinear(b);
  // sRGB D65 → XYZ
  let x = (rl * 0.4124 + gl * 0.3576 + bl * 0.1805) / 0.95047;
  let y = rl * 0.2126 + gl * 0.7152 + bl * 0.0722;
  let z = (rl * 0.0193 + gl * 0.1192 + bl * 0.9505) / 1.08883;
  const f = (t: number) =>
    t > 0.008856 ? Math.cbrt(t) : 7.787 * t + 16 / 116;
  x = f(x);
  y = f(y);
  z = f(z);
  return [116 * y - 16, 500 * (x - y), 200 * (y - z)];
}

/** Precomputed Lab values for the table (lazy, one-time). */
let labTable: [number, number, number][] | null = null;

function hexToRgbTuple(hex: string): [number, number, number] | null {
  const m = /^#?([0-9a-f]{2})([0-9a-f]{2})([0-9a-f]{2})$/i.exec(hex.trim());
  if (!m) return null;
  return [parseInt(m[1]!, 16), parseInt(m[2]!, 16), parseInt(m[3]!, 16)];
}

export interface DmcMatch {
  code: string;
  name: string;
  /** CIELAB distance — smaller is closer; < 10 is a good match */
  distance: number;
}

/** Nearest DMC floss colour for a #rrggbb value (CIELAB distance). */
export function nearestDmc(hex: string): DmcMatch | null {
  const rgb = hexToRgbTuple(hex);
  if (!rgb) return null;
  if (!labTable) {
    labTable = DMC_COLORS.map((c) => rgbToLab(c.rgb[0], c.rgb[1], c.rgb[2]));
  }
  const [l, a, b] = rgbToLab(rgb[0], rgb[1], rgb[2]);
  let best = -1;
  let bestD = Infinity;
  for (let i = 0; i < DMC_COLORS.length; i++) {
    const t = labTable[i]!;
    const d =
      (l - t[0]) * (l - t[0]) +
      (a - t[1]) * (a - t[1]) +
      (b - t[2]) * (b - t[2]);
    if (d < bestD) {
      bestD = d;
      best = i;
    }
  }
  if (best < 0) return null;
  const c = DMC_COLORS[best]!;
  return { code: c.code, name: c.name, distance: Math.sqrt(bestD) };
}
